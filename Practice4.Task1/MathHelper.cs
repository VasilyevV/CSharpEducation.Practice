namespace Practice4
{
    public class MathHelper
    {
        public static int Add(int a, int b)
        {
            return a + b;
        }
        public static int Sub(int a, int b)
        {
            return a - b;
        }
        public static int Mult(int a, int b)
        {
            return a * b;
        }
        public static int Divide(int a, int b)
        {
            return (int)a / b;
        }
    }
}